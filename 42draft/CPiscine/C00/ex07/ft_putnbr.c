#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	to_char;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	to_char = nb % 10 + '0';
	write(1, &to_char, 1);
}

int main (){
	ft_putnbr(52);
}